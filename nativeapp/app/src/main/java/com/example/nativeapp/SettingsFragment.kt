class SettingsFragment : Fragment() {
    // TODO: Rename and change types of parameters
    private var param1: String? = null
    private var param2: String? = null
    var editText: EditText? =null
    var n:Int=0


    override fun onCreate(savedInstanceState: Bundle?) {

        super.onCreate(savedInstanceState)
        arguments?.let {
            param1 = it.getString(ARG_PARAM1)
            param2 = it.getString(ARG_PARAM2)
        }

    }

    override fun onCreateView(
        inflater: LayoutInflater, container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View? {
        // Inflate the layout for this fragment
        return inflater.inflate(R.layout.fragment_settings, container, false)
    }

    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)
        val rv= getView()?.findViewById<RecyclerView>(R.id.recycler_view)
        val layoutManager=LinearLayoutManager(context)
        rv?.layoutManager=layoutManager
        editText = getView()?.findViewById<EditText>(R.id.number)


        var adapter=MyAdapter(n)
        rv?.adapter=adapter;

        var button=getView()?.findViewById<Button>(R.id.add_one)
        button?.setOnClickListener(View.OnClickListener { view ->
            n=Integer.parseInt(editText?.text.toString());
            adapter.changeNum(n);
            adapter.notifyDataSetChanged();
        })
    }

